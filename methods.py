def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

def sum_of_values(valor1, valor2):
    sum = valor1 + valor2
    return sum

def sub_of_values(valor1, valor2):
    sub = valor1 - valor2
    return sub

def mult_of_values(valor1, valor2):
    mult = valor1 * valor2
    return mult

def div_of_values(valor1, valor2):
    div = valor1 / valor2
    return div
